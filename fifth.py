#Relational Operators
# ==, !=, >, <, >=, <=
A=5
B=5
C=4
D=6
print(A==B)
print(A!=B)
print(A==C)
print(A<C)
print(A<=B)
print(D>A)
print(D>=B)

#Assignment Operators
# =, +=, -=,*=,/=, %=,**=
M=10
M=M+10
print(M)
M+=10
print(M)
M-=10
print(M)
N=4
N*=2
print(N)
N**=2
print(N)
N/=2
print(N)
N%=2
print(N)


#Logical operators
# not and or
print(not True)
print(not False)
a=20
b=10
print(not (a>b))
c=True
d=False
print((a>b) and c or d)




